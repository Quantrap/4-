#include <iostream>
using namespace std;

int main()
{
    setlocale(NULL, "rus");
    double x, sin;
    int n;
    cout << "Введите число x от которого нужно найти синус, и количество шагов n \n";
    cin >> x >> n;
    sin = x;
    while (n >= 2)
    {
        if (n % 2 == 0) sin += pow(x, 2*n+1) / (2*n+1);
        else  sin -= pow(x, 2 * n + 1) / 2 * n + 1;
        n -= 1;
    }
    cout << "Синус= " << sin << "\n";
    system("pause");
}

